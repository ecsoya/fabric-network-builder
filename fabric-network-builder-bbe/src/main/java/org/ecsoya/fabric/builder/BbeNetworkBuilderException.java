package org.ecsoya.fabric.builder;

public class BbeNetworkBuilderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3303490998239593821L;

	public BbeNetworkBuilderException() {
		super();
	}

	public BbeNetworkBuilderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BbeNetworkBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public BbeNetworkBuilderException(String message) {
		super(message);
	}

	public BbeNetworkBuilderException(Throwable cause) {
		super(cause);
	}

}
